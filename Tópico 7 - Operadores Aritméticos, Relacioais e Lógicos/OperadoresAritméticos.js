// Operadores Aritméticos

// - Operadores usados para realizar operações matemáticas

// Adição "+": Soma os valores;
// Subtração "-": Subtrai os valores;
// Multiplicação "*": Multiplica os valores;
// Divisão "/": Divide os valores;
// Módulo "%": Retorna o resto da divisão dos valores;

let num1 = 10;
let num2 = 5;

// SOMA

let soma = num1 + num2;

console.log("A soma dos valores é: " + soma);

// Saída: A soma dos valores é: 15


// SUBTRAÇÃO

let subtracao = num1 - num2;

console.log("A subtração dos valores é: " + subtracao);

// Saída: A subtração dos valores é: 5


// MULTIPLICAÇÃO

let multiplicacao = num1 * num2;

console.log("A multiplicação dos valores é: " + multiplicacao);

// Saída: A multiplicação dos valores é: 50


// DIVISÃO

let divisao = num1 / num2;

console.log("A divisão dos valores é: " + divisao);

// Saída: A divisão dos valores é: 2


// MÓDULO

let modulo = num1 % num2;

console.log("O módulo dos valores é: " + modulo);

// Saída: O módulo dos valores é: 0